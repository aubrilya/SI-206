                            <p>During my undergraute years at UofM I have kept a high academic standard for myself and have obtained both honors and Angel Scholar awards. 
                                During my senior year, I have decided to take on the challenge of writing an honors thesis on how
                                entrepreneurial nonprofits in Dehradun, India affect the social status of women. </p>
                            <p>My undergraduate time at the University of Michigan has undoubtedly followed a theme of 
                                entrepreneurship. From my freshman year until now, the idea of entrepreneurship has slowly 
                                evolved from a personal adventure often involving self-gain and competition to realizing that 
                            it can play an incredibly strong role in shaping our societal standards. Entrepreneurship can heavily alter organizational or 
                            political structures by re-inventing the distribution of resources, networks available, and the mindset of the involved actors. 
                            In Victoria Johnson's class, I encountered the powerful idea that nonprofits and entrepreneurship are often intermingled and I found myself 
                            drawn for the first time to the mission of empowering women in society by giving them a product or job that could lead to their autonomy. <br> <br></p>
                            <p>This summer I am excited to work in rural India near Dehradun, where I will be helping a nonprofit named Ankuri fulfill this 
                            very mission. The compassionate side of entrepreneurship can be brought to the table through nonprofits and I am excited to 
                            learn about how this can be done more successfully within the confines of a complex network of organizations, 
                            social structures, and economic constraints in an underdeveloped country. </p>

                            
